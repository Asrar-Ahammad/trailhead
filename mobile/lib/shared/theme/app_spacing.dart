/// Spacing scale for the Trailhead design system.
/// 4px base unit — use only these constants for padding/gap values in widget code.
abstract final class AppSpacing {
  /// 4px — tight icon/text gaps
  static const double xs = 4;

  /// 8px — internal component padding
  static const double sm = 8;

  /// 16px — standard card/section padding, default gap between stacked elements
  static const double md = 16;

  /// 24px — section separation
  static const double lg = 24;

  /// 32px — major layout breaks
  static const double xl = 32;

  /// 48px — screen-top breathing room
  static const double xxl = 48;
}
